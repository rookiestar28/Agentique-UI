#!/usr/bin/env node
import fs from "node:fs";
import { validatePublicReleaseName, validateReleasePublicationPolicy, validateReleaseTag } from "../src/core/release-boundary.mjs";

const packageJson = JSON.parse(fs.readFileSync("package.json", "utf8"));
const policyDoc = fs.readFileSync("docs/governance/release-publication-policy.md", "utf8");
const boundaryDoc = fs.readFileSync("docs/governance/repository-boundary.md", "utf8");

const failures = [];
const policy = validateReleasePublicationPolicy({ packageJson });
if (!policy.ok) failures.push(...policy.findings.map((finding) => `${finding.code}: ${finding.message}`));

for (const phrase of [
  "public source repository",
  "npm package is intentionally marked private",
  "Apache-2.0 source license",
  "draft release may be generated by automation",
  "stable public release requires maintainer review"
]) {
  if (!policyDoc.includes(phrase)) {
    failures.push(`release-publication-policy.md missing: ${phrase}`);
  }
}

for (const phrase of [
  "independent public repository line",
  "separate Agentique web/catalog systems",
  "must not claim an installable production desktop runtime"
]) {
  if (!boundaryDoc.includes(phrase)) {
    failures.push(`repository-boundary.md missing: ${phrase}`);
  }
}

for (const sample of ["Agentique UI v0.1.0", "Agentique UI v0.1.0 release candidate"]) {
  const result = validatePublicReleaseName(sample);
  if (!result.ok) failures.push(`safe release name rejected: ${sample}`);
}

for (const tag of ["v0.1.0", "v0.1.0-rc.1"]) {
  const result = validateReleaseTag(tag);
  if (!result.ok) failures.push(`safe release tag rejected: ${tag}`);
}

for (const sample of [`Agentique UI ${"R"}1234`, "Agentique UI released installer", "Agentique UI signed updater is available"]) {
  const result = validatePublicReleaseName(sample);
  if (result.ok) failures.push(`unsafe release name accepted: ${sample}`);
}

if (failures.length > 0) {
  console.error(JSON.stringify({ status: "failed", failures }, null, 2));
  process.exit(1);
}

console.log(JSON.stringify({
  status: "passed",
  checked: [
    "package.json",
    "docs/governance/release-publication-policy.md",
    "docs/governance/repository-boundary.md",
    "release name/tag rules"
  ]
}, null, 2));
